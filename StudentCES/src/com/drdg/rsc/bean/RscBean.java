package com.drdg.rsc.bean;


public class RscBean {

	private String rsc_id;
	private String stu_name;
	private String cat_name;
	private String rsc_o_fraction;
	private String rsc_weight;
	private String rsc_fraction;
	private String rsc_date;

	public String getRsc_id() {
		return rsc_id;
	}

	public void setRsc_id(String rscId) {
		rsc_id = rscId;
	}

	public String getStu_name() {
		return stu_name;
	}

	public void setStu_name(String stuName) {
		stu_name = stuName;
	}

	public String getCat_name() {
		return cat_name;
	}

	public void setCat_name(String catName) {
		cat_name = catName;
	}

	public String getRsc_o_fraction() {
		return rsc_o_fraction;
	}

	public void setRsc_o_fraction(String rscOFraction) {
		rsc_o_fraction = rscOFraction;
	}

	public String getRsc_weight() {
		return rsc_weight;
	}

	public void setRsc_weight(String rscWeight) {
		rsc_weight = rscWeight;
	}

	public String getRsc_fraction() {
		return rsc_fraction;
	}

	public void setRsc_fraction(String rscFraction) {
		rsc_fraction = rscFraction;
	}

	public String getRsc_date() {
		return rsc_date;
	}

	public void setRsc_date(String rscDate) {
		rsc_date = rscDate;
	}

}
